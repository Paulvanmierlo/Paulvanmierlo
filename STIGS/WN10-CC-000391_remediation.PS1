# Define the registry path and values
$regPath = "HKLM:\SOFTWARE\Policies\Microsoft\Internet Explorer\Main"
$regName = "DisableIE"
$regValue = 1  # Enables policy: Disable IE11 as standalone browser

# Ensure script runs as Administrator
if (-not ([Security.Principal.WindowsPrincipal][Security.Principal.WindowsIdentity]::GetCurrent()).IsInRole(
    [Security.Principal.WindowsBuiltInRole]::Administrator)) {
    Write-Warning "This script must be run as Administrator. Exiting..."
    exit 1
}

# Create the registry path if it doesn't exist
if (-not (Test-Path $regPath)) {
    New-Item -Path $regPath -Force | Out-Null
}

# Set the registry value
New-ItemProperty -Path $regPath -Name $regName -PropertyType DWord -Value $regValue -Force | Out-Null

Write-Output "âœ… STIG WN10-CC-000391 remediated: Internet Explorer 11 is now disabled as a standalone browser."
